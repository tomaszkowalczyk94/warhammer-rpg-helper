CREATE TABLE PERSONTOSKILL (
	ID         INTEGER AUTO_INCREMENT
		PRIMARY KEY,
	PERSON_ID  INTEGER,
	SKILL_ID   INTEGER,
	PLUSTEN    TINYINT,
	PLUSTWENTY TINYINT
)
;

CREATE TABLE WEAPON (
	ID             INTEGER AUTO_INCREMENT
		PRIMARY KEY,
	NAME           VARCHAR(255) NOT NULL,
	LOAD           INTEGER,
	CATEGORY       VARCHAR(100),
	DAMAGE         INTEGER,
	RANGE          INTEGER,
	RELOADTIME     INTEGER,
	WEAPONFEATURES VARCHAR(255)
)
;

CREATE UNIQUE INDEX WEAPON_NAME_IDX
	ON WEAPON (NAME)
;

CREATE TABLE PERSONTOWEAPON (
	ID        INTEGER AUTO_INCREMENT
		PRIMARY KEY,
	PERSON_ID INTEGER,
	WEAPON_ID INTEGER
)
;

CREATE TABLE EQUIPMENT (
	ID               INTEGER AUTO_INCREMENT
		PRIMARY KEY,
	ITEM             VARCHAR(255),
	EQUIPLOAD        VARCHAR(255),
	EQUIPDESCRIPTION VARCHAR(255),
