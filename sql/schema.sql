CREATE TABLE SKILL (
	ID                   INTEGER AUTO_INCREMENT
		PRIMARY KEY,
	NAME                 VARCHAR(255) NOT NULL,
	DESCRIPTION          TEXT,
	ISADVANCED           TINYINT,
	RELATEDTALENTSSTRING VARCHAR(255),
	CHARACTERISTIC       VARCHAR(255)
)
;

CREATE UNIQUE INDEX SKILL_NAME_IDX
	ON SKILL (NAME)
;

